package cogmem

import (
	"context"
	"fmt"
	"strings"

	"github.com/PivotLLM/ClawEh/pkg/cogmem/store"
)

// renderFullExport renders the agent's entire active cognitive memory as one
// Markdown document: the always-on general domain, every topic domain (with its
// state, tool/keyword triggers, and memories), and the pending-confirmation digest. It returns
// the document plus the domain and memory counts for the tool's summary line.
func renderFullExport(ctx context.Context, s *store.Store) (doc string, nDomains, nMemories int, err error) {
	db := s.DB()
	domains, err := s.ListDomains(ctx, db, store.StatusActive)
	if err != nil {
		return "", 0, 0, err
	}

	var sticky, topics []store.Domain
	for _, d := range domains {
		if d.Sticky() {
			sticky = append(sticky, d)
		} else {
			topics = append(topics, d)
		}
	}

	var b strings.Builder
	b.WriteString("# Cognitive Memory Export\n\n")
	b.WriteString("<!-- Snapshot of active memory generated by ClawEh. -->\n\n")

	section := func(title string, ds []store.Domain) error {
		if len(ds) == 0 {
			return nil
		}
		fmt.Fprintf(&b, "## %s\n\n", title)
		for _, d := range ds {
			mems, err := s.ListMemories(ctx, db, d.ID, store.StatusActive)
			if err != nil {
				return err
			}
			nDomains++
			nMemories += len(mems)
			label := "topic"
			if d.Sticky() {
				label = "sticky"
			}
			fmt.Fprintf(&b, "### %s · `%s` (%s)\n\n", d.Name, d.ID, label)
			if sm := strings.TrimSpace(d.Summary); sm != "" {
				fmt.Fprintf(&b, "%s\n\n", sm)
			}
			writeList(&b, "Blockers", d.State.Blockers)
			writeList(&b, "Next actions", d.State.NextActions)
			writeList(&b, "Constraints", d.State.Constraints)
			if d.Triggers != "" {
				fmt.Fprintf(&b, "Tool triggers (auto-load when a matching tool is used): `%s`\n\n", d.Triggers)
			}
			if d.KeywordTriggers != "" {
				fmt.Fprintf(&b, "Keyword triggers (auto-load when a phrase appears in the message): `%s`\n\n", d.KeywordTriggers)
			}
			if len(mems) == 0 {
				b.WriteString("_(no memories)_\n\n")
				continue
			}
			for _, m := range mems {
				fmt.Fprintf(&b, "- (%s) %s\n", m.Type, strings.TrimSpace(m.Text))
			}
			b.WriteString("\n")
		}
		return nil
	}

	if err := section("Sticky (always in context)", sticky); err != nil {
		return "", 0, 0, err
	}
	if err := section("Topics", topics); err != nil {
		return "", 0, 0, err
	}

	// Pending confirmation digest.
	pending, err := s.ListPending(ctx, db, 0)
	if err != nil {
		return "", 0, 0, err
	}
	if len(pending) > 0 {
		b.WriteString("## Pending Confirmation\n\n")
		b.WriteString("Inferred from conversation, awaiting confirmation (not yet acted on as rules).\n\n")
		for _, m := range pending {
			fmt.Fprintf(&b, "- (%s, confidence %.2f) %s\n", m.Type, m.Confidence, strings.TrimSpace(m.Text))
		}
		b.WriteString("\n")
	}

	return strings.TrimRight(b.String(), "\n") + "\n", nDomains, nMemories, nil
}

func writeList(b *strings.Builder, label string, items []string) {
	if len(items) == 0 {
		return
	}
	fmt.Fprintf(b, "%s:\n", label)
	for _, x := range items {
		fmt.Fprintf(b, "- %s\n", x)
	}
	b.WriteString("\n")
}
